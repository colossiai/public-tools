"""
13 · 贪心 —— 跳跃游戏 & 分发饼干

═══════════════════════════════════════════════════════════════
【题目】 LeetCode 55 跳跃游戏 / LeetCode 455 分发饼干
═══════════════════════════════════════════════════════════════

LC55 跳跃游戏：数组 nums[i] 表示在下标 i 处最多能向右跳的步数，
从下标 0 出发，问能否到达最后一个下标。

    nums = [2, 3, 1, 1, 4]  ->  True
    nums = [3, 2, 1, 0, 4]  ->  False   （必到下标 3 的 0，卡死）

LC455 分发饼干：孩子胃口 g[i]，饼干尺寸 s[j]，一块饼干只能给一个
孩子，且 s[j] >= g[i] 才能喂饱。问最多喂饱几个孩子。

    g = [1, 2, 3], s = [1, 1]  ->  1

═══════════════════════════════════════════════════════════════
【什么时候想到这个套路】
═══════════════════════════════════════════════════════════════

信号：每一步都能凭「当下最划算」的选择直接推进，且能论证这个局部
最优不会毁掉全局最优。常常配合「排序」预处理。

⚠️ 关键前提：贪心正确性必须能证明（或至少找不到反例）。
「局部最优 → 全局最优」不成立时贪心会给出错误答案——例如凑硬币
coins=[1,3,4] 凑 6，贪心先拿 4 会得 4+1+1=3 枚，最优其实是 3+3=2 枚。
拿不准就用 DP 保底（DP 枚举所有决策，一定正确，代价是更高复杂度）。

═══════════════════════════════════════════════════════════════
【思路】
═══════════════════════════════════════════════════════════════

  LC55：维护「当前能到达的最远下标 reach」。从左往右扫，只要 i 在
        reach 之内就用 i + nums[i] 更新 reach；一旦 i 超过 reach
        说明前面所有位置都跳不到 i，断路。
        证明：能到 i 就必能到 0..i 的每个点，所以只需跟踪最远端。

  LC455：两边都排序，用最小的饼干去喂胃口最小的孩子。
        证明：把大饼干浪费在小胃口孩子上绝不会更优——留着大饼干总能
        喂饱同样或更多的孩子，所以「小饼干配小胃口」是安全的贪心。

═══════════════════════════════════════════════════════════════
【复杂度】
═══════════════════════════════════════════════════════════════

  LC55：一次线性扫描，时间 O(n)，空间 O(1)。
  LC455：瓶颈在排序，时间 O(n log n + m log m)，空间 O(1)（不计排序）。
"""
from __future__ import annotations


# ────────────────────────────────────────────────────────────
# LC55 跳跃游戏
# ────────────────────────────────────────────────────────────

def can_jump(nums: list[int]) -> bool:
    """能否从下标 0 跳到最后一个下标。"""
    reach = 0  # 目前从 0 出发能触及的最远下标
    for i, step in enumerate(nums):
        # 局部最优：只要 i 还在能触及范围内，就贪心地把 reach 拉到最大。
        if i > reach:
            # i 已经超出能到达的边界，说明中间有个 0 把路彻底堵死。
            return False
        # i + step 是站在 i 处能到的最远点；取最大值即全局最远。
        if i + step > reach:
            reach = i + step
        # 小优化：已经能覆盖终点就可以提前收工。
        if reach >= len(nums) - 1:
            return True
    return True


# ────────────────────────────────────────────────────────────
# LC455 分发饼干
# ────────────────────────────────────────────────────────────

def find_content_children(g: list[int], s: list[int]) -> int:
    """最多能喂饱的孩子数。"""
    # 排序是贪心的常见前置动作：让「最小配最小」这个策略能顺序执行。
    g = sorted(g)
    s = sorted(s)

    child = 0  # 当前尝试满足的孩子指针（指向胃口从小到大）
    for cookie in s:
        # 依次拿出从小到大的饼干：
        # 若当前这块能喂饱当前最小胃口的孩子，就喂给他，孩子指针前移。
        if child < len(g) and cookie >= g[child]:
            child += 1
        # 否则这块饼干太小、喂不了当前孩子（更别说后面胃口更大的），丢弃。
    return child  # 指针走了多远 = 喂饱了几个孩子


# ────────────────────────────────────────────────────────────
# 主程序：断言正确性 + 打印可读轨迹
# ────────────────────────────────────────────────────────────

if __name__ == "__main__":
    # ---- 正确性断言 ----
    assert can_jump([2, 3, 1, 1, 4]) is True
    assert can_jump([3, 2, 1, 0, 4]) is False
    assert can_jump([0]) is True          # 已在终点
    assert can_jump([1, 0, 1]) is False   # 到下标 1 的 0 卡死
    assert can_jump([2, 0, 0]) is True

    assert find_content_children([1, 2, 3], [1, 1]) == 1
    assert find_content_children([1, 2], [1, 2, 3]) == 2
    assert find_content_children([10, 9, 8, 7], [5, 6, 7, 8]) == 2
    assert find_content_children([], [1, 2]) == 0

    print("=" * 60)
    print("13 · 贪心 —— 跳跃游戏 & 分发饼干")
    print("=" * 60)

    print("\n【LC55 跳跃游戏】跟踪能到达的最远下标 reach")
    nums = [2, 3, 1, 1, 4]
    reach = 0
    for i, step in enumerate(nums):
        blocked = i > reach
        reach = max(reach, i + step) if not blocked else reach
        print(f"  i={i} nums[i]={step} -> reach={reach}"
              + ("  (断路!)" if blocked else ""))
    print(f"  终点下标 {len(nums) - 1}，reach={reach} -> 可达 = {reach >= len(nums) - 1}")

    print("\n【LC455 分发饼干】排序后小饼干配小胃口")
    g, s = [1, 2, 3], [1, 1]
    print(f"  胃口 g = {sorted(g)}，饼干 s = {sorted(s)}")
    print(f"  最多喂饱 = {find_content_children(g, s)} 个孩子")

    print("\n提示：贪心必须能论证局部最优→全局最优，否则改用 DP 保底。")
    print("\n全部断言通过 ✅")
