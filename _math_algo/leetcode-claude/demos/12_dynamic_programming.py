"""
12 · 动态规划 —— 爬楼梯 & 打家劫舍

═══════════════════════════════════════════════════════════════
【题目】 LeetCode 70 爬楼梯 / LeetCode 198 打家劫舍
═══════════════════════════════════════════════════════════════

LC70 爬楼梯：每次可以爬 1 或 2 个台阶，问爬到第 n 阶有多少种不同方法。

    n = 3  ->  3   （1+1+1 / 1+2 / 2+1）

LC198 打家劫舍：一排房子每家有钱 nums[i]，相邻两家不能同时偷
（会触发警报），问最多能偷多少钱。

    nums = [2, 7, 9, 3, 1]  ->  12   （偷 2 + 9 + 1）

═══════════════════════════════════════════════════════════════
【什么时候想到这个套路】
═══════════════════════════════════════════════════════════════

两个信号同时出现就基本是 DP：
  1. 最优子结构：大问题的最优解由子问题的最优解拼出来。
  2. 重叠子问题：朴素递归会把同一个子问题算很多遍。

题面常出现「多少种方法 / 最多 / 最少 / 能否」这类最值、计数、
可行性的字眼，而且当前决策只依赖前面若干步的结果。

═══════════════════════════════════════════════════════════════
【思路】 DP 五步法
═══════════════════════════════════════════════════════════════

  1️⃣ 定义状态：dp[i] 表示「到第 i 个位置」的答案。
  2️⃣ 状态转移方程：dp[i] 由前面的状态推出。
  3️⃣ 初始化：把最小的边界状态填好。
  4️⃣ 遍历顺序：保证算 dp[i] 时它依赖的状态都已算好。
  5️⃣ 返回值：最终要的那个状态。

本文件只做入门示范，保持简短。更系统的 DP 教程（记忆化、滚动数组、
背包、区间 DP、状压 DP 等）见 `../../dp-tutorial-claude/`。

═══════════════════════════════════════════════════════════════
【复杂度】
═══════════════════════════════════════════════════════════════

两题都是一维线性 DP：时间 O(n)，用滚动变量后空间 O(1)。
"""
from __future__ import annotations


# ────────────────────────────────────────────────────────────
# LC70 爬楼梯
# ────────────────────────────────────────────────────────────

def climb_stairs(n: int) -> int:
    """爬到第 n 阶的方法数。"""
    # 1️⃣ 状态：dp[i] = 爬到第 i 阶的方法数。
    # 2️⃣ 转移：最后一步要么从 i-1 迈 1 阶，要么从 i-2 迈 2 阶，
    #    两种路径互不重叠，所以方法数相加：dp[i] = dp[i-1] + dp[i-2]。
    #    （本质就是斐波那契。）
    # 3️⃣ 初始化：dp[0] = 1（站在地面，什么都不爬也算 1 种），dp[1] = 1。
    if n <= 1:
        return 1

    # 4️⃣ 遍历顺序：从小到大，算 dp[i] 时 dp[i-1]、dp[i-2] 已就绪。
    #    只依赖前两个状态，所以用两个滚动变量代替整个数组，空间降到 O(1)。
    prev2, prev1 = 1, 1  # 分别代表 dp[i-2]、dp[i-1]
    for _ in range(2, n + 1):
        prev2, prev1 = prev1, prev1 + prev2

    # 5️⃣ 返回值：dp[n]，此刻存在 prev1 里。
    return prev1


# ────────────────────────────────────────────────────────────
# LC198 打家劫舍
# ────────────────────────────────────────────────────────────

def rob(nums: list[int]) -> int:
    """相邻不能同时偷，求最大金额。"""
    # 1️⃣ 状态：dp[i] = 只考虑前 i 家（下标 0..i-1）能偷到的最大金额。
    # 2️⃣ 转移：对第 i 家（下标 i-1）做「偷 or 不偷」的决策：
    #      - 不偷它：继承 dp[i-1]；
    #      - 偷它：  不能碰第 i-1 家，只能接 dp[i-2] 再加上自己 nums[i-1]。
    #    取两者较大：dp[i] = max(dp[i-1], dp[i-2] + nums[i-1])。
    #    这就是「局部最优」——为什么贪心「每家都偷大的」会失效：见下方 demo。
    # 3️⃣ 初始化：dp[0] = 0（没有房子），dp[1] = nums[0]（只有一家就偷它）。
    prev2, prev1 = 0, 0  # dp[i-2]、dp[i-1]
    for money in nums:
        # 4️⃣ 遍历顺序：从左到右扫房子。
        prev2, prev1 = prev1, max(prev1, prev2 + money)

    # 5️⃣ 返回值：dp[n]。
    return prev1


# ────────────────────────────────────────────────────────────
# 主程序：断言正确性 + 打印可读轨迹
# ────────────────────────────────────────────────────────────

if __name__ == "__main__":
    # ---- 正确性断言 ----
    assert climb_stairs(2) == 2       # 1+1 / 2
    assert climb_stairs(3) == 3       # 1+1+1 / 1+2 / 2+1
    assert climb_stairs(5) == 8       # 斐波那契：1,2,3,5,8
    assert climb_stairs(1) == 1
    assert climb_stairs(0) == 1

    assert rob([2, 7, 9, 3, 1]) == 12  # 2 + 9 + 1
    assert rob([1, 2, 3, 1]) == 4      # 1 + 3
    assert rob([]) == 0
    assert rob([5]) == 5
    assert rob([2, 1, 1, 2]) == 4      # 2 + 2

    print("=" * 60)
    print("12 · 动态规划 —— 爬楼梯 & 打家劫舍")
    print("=" * 60)

    print("\n【LC70 爬楼梯】dp[i] = dp[i-1] + dp[i-2]")
    dp = [1, 1]
    for i in range(2, 8):
        dp.append(dp[i - 1] + dp[i - 2])
    for i in range(1, 8):
        print(f"  爬到第 {i} 阶：{dp[i]} 种方法")

    print("\n【LC198 打家劫舍】nums = [2, 7, 9, 3, 1]")
    nums = [2, 7, 9, 3, 1]
    prev2, prev1 = 0, 0
    for i, money in enumerate(nums):
        cur = max(prev1, prev2 + money)
        # 展示「偷/不偷」的对比，理解为什么不能贪心地每次偷最大
        choice = "偷它" if prev2 + money > prev1 else "跳过"
        print(f"  看到第 {i} 家(${money})：不偷={prev1} 偷={prev2 + money} -> {choice}，dp={cur}")
        prev2, prev1 = prev1, cur
    print(f"  最多能偷 = {prev1}（贪心地先偷最大的 9，会挡住 7 和 3，反而不划算）")

    print("\n更系统的 DP 教程见 ../../dp-tutorial-claude/")
    print("\n全部断言通过 ✅")
