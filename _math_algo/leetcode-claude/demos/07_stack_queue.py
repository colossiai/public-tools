"""
07 · 栈 / 队列 —— 有效的括号

═══════════════════════════════════════════════════════════════
【题目】 LeetCode 20. 有效的括号
═══════════════════════════════════════════════════════════════

给一个只含 '()[]{}' 的字符串，判断括号是否有效：
  1. 每个左括号都有同类型的右括号闭合；
  2. 闭合顺序正确（后开的先闭，即「最近未匹配的左括号先匹配」）。

例子：

    "()"       → True
    "()[]{}"   → True
    "(]"       → False   （类型不匹配）
    "([)]"     → False   （交叉，闭合顺序错）
    "{[]}"     → True

═══════════════════════════════════════════════════════════════
【什么时候想到这个套路】
═══════════════════════════════════════════════════════════════

出现「最近的、最里层的、后进先出」的配对/撤销/嵌套语义就想到栈：
  - 括号 / 标签匹配、表达式求值、路径简化（"../"）。
  - 「撤销上一步」「回到最近的状态」。
队列（先进先出）则对应「按到达顺序处理」「BFS 层序」等。

═══════════════════════════════════════════════════════════════
【思路】用栈记住「还没被闭合的左括号」
═══════════════════════════════════════════════════════════════

从左到右扫描：
  - 遇到左括号 → 压栈（记下「等待被闭合」的它）。
  - 遇到右括号 → 它必须闭合「最近的那个左括号」，也就是栈顶：
      * 栈空 → 没有左括号可配，非法；
      * 栈顶类型不匹配 → 非法；
      * 匹配 → 弹出栈顶。
  - 扫完后栈必须为空（否则还有左括号没闭合）。

用一张「右→左」的映射表，遇到右括号时直接查它该配哪个左括号。

    s = "{[]}"
    '{' 压栈        栈: ['{']
    '[' 压栈        栈: ['{','[']
    ']' 配栈顶'[' ✓ 栈: ['{']
    '}' 配栈顶'{' ✓ 栈: []
    结束栈空 → 有效

═══════════════════════════════════════════════════════════════
【附例 1】 LeetCode 150. 逆波兰表达式求值 —— 用栈算式
═══════════════════════════════════════════════════════════════

逆波兰式（后缀表达式）里运算符跟在操作数后面，天生适合栈：
  - 遇到数字 → 压栈；
  - 遇到运算符 → 弹出栈顶两个数计算，结果再压回栈。

    tokens = ["2","1","+","3","*"]  →  (2+1)*3 = 9

注意减法/除法的操作数顺序：先弹出的是右操作数（b），后弹的是左（a），
算 a op b；除法向零截断。

═══════════════════════════════════════════════════════════════
【附例 2】 LeetCode 155. 最小栈 —— 辅助栈 O(1) 取最小
═══════════════════════════════════════════════════════════════

设计一个栈，push/pop/top/getMin 都是 O(1)。
诀窍：再开一个「同步的最小值栈」min_stack，栈顶始终是当前主栈的最小值。
push x 时压入 min(x, 当前最小)；pop 时两栈同步弹出。

═══════════════════════════════════════════════════════════════
【复杂度】
═══════════════════════════════════════════════════════════════

  LC20 : 时间 O(n)  空间 O(n)
  LC150: 时间 O(n)  空间 O(n)
  LC155: 每个操作 O(1)  空间 O(n)
"""
from __future__ import annotations


# ────────────────────────────────────────────────────────────
# 主例：LC20 有效的括号
# ────────────────────────────────────────────────────────────

def is_valid(s: str) -> bool:
    """判断括号串是否有效闭合。"""
    # 右括号 → 对应左括号；遇到右括号时用来核对栈顶
    pairs = {")": "(", "]": "[", "}": "{"}
    stack: list[str] = []

    for ch in s:
        if ch in pairs:
            # 右括号：栈顶必须是与之匹配的左括号，否则非法
            # 栈空时用占位符 '#'，保证比较结果为 False
            if not stack or stack.pop() != pairs[ch]:
                return False
        else:
            # 左括号：压栈等待闭合
            stack.append(ch)

    # 全部扫完，栈必须为空（无残留未闭合的左括号）
    return not stack


# ────────────────────────────────────────────────────────────
# 附例 1：LC150 逆波兰表达式求值
# ────────────────────────────────────────────────────────────

def eval_rpn(tokens: list[str]) -> int:
    """计算逆波兰（后缀）表达式的值。"""
    ops = {"+", "-", "*", "/"}
    stack: list[int] = []

    for tok in tokens:
        if tok in ops:
            # 先弹的是右操作数 b，后弹的是左操作数 a，注意顺序
            b = stack.pop()
            a = stack.pop()
            if tok == "+":
                stack.append(a + b)
            elif tok == "-":
                stack.append(a - b)
            elif tok == "*":
                stack.append(a * b)
            else:
                # 题目要求除法向零截断，用 int(a / b) 而非 //（后者向下取整）
                stack.append(int(a / b))
        else:
            stack.append(int(tok))

    # 合法表达式结束时栈里恰好剩一个结果
    return stack[-1]


# ────────────────────────────────────────────────────────────
# 附例 2：LC155 最小栈
# ────────────────────────────────────────────────────────────

class MinStack:
    """支持 push/pop/top/get_min 均为 O(1) 的栈。"""

    def __init__(self) -> None:
        self._data: list[int] = []
        # 与主栈同步：栈顶永远是「当前主栈里的最小值」
        self._min: list[int] = []

    def push(self, x: int) -> None:
        self._data.append(x)
        # 压入「x 与当前最小」的较小者，从而 O(1) 维护最小值
        cur_min = x if not self._min else min(x, self._min[-1])
        self._min.append(cur_min)

    def pop(self) -> None:
        self._data.pop()
        self._min.pop()          # 两栈同步弹出，保持对齐

    def top(self) -> int:
        return self._data[-1]

    def get_min(self) -> int:
        return self._min[-1]


# ────────────────────────────────────────────────────────────
# 自测 + 演示
# ────────────────────────────────────────────────────────────

if __name__ == "__main__":
    # ---- LC20 正确性 ----
    assert is_valid("()") is True
    assert is_valid("()[]{}") is True
    assert is_valid("(]") is False
    assert is_valid("([)]") is False
    assert is_valid("{[]}") is True
    assert is_valid("") is True
    assert is_valid("(") is False

    # ---- LC150 正确性 ----
    assert eval_rpn(["2", "1", "+", "3", "*"]) == 9
    assert eval_rpn(["4", "13", "5", "/", "+"]) == 6
    assert eval_rpn(["10", "6", "9", "3", "+", "-11", "*", "/", "*", "17", "+", "5", "+"]) == 22
    assert eval_rpn(["6", "-7", "/"]) == 0      # 向零截断：-0 → 0

    # ---- LC155 正确性 ----
    ms = MinStack()
    ms.push(-2); ms.push(0); ms.push(-3)
    assert ms.get_min() == -3
    ms.pop()
    assert ms.top() == 0
    assert ms.get_min() == -2

    print("=" * 63)
    print("LC20 · 有效的括号")
    print("=" * 63)
    for test in ["()", "()[]{}", "(]", "([)]", "{[]}"]:
        print(f"  {test:<8} → {is_valid(test)}")

    print("\n" + "=" * 63)
    print("LC150 · 逆波兰表达式求值")
    print("=" * 63)
    tokens = ["2", "1", "+", "3", "*"]
    print(f"tokens = {tokens}   （中缀：(2 + 1) * 3）")
    stack: list[int] = []
    for tok in tokens:
        if tok in {"+", "-", "*", "/"}:
            b = stack.pop(); a = stack.pop()
            r = {"+": a + b, "-": a - b, "*": a * b, "/": int(a / b)}[tok]
            stack.append(r)
            print(f"  运算 {a} {tok} {b} = {r:<3} 栈={stack}")
        else:
            stack.append(int(tok))
            print(f"  压入 {tok:<10} 栈={stack}")
    print(f"结果 = {stack[-1]}")

    print("\n" + "=" * 63)
    print("LC155 · 最小栈")
    print("=" * 63)
    ms = MinStack()
    for x in [-2, 0, -3]:
        ms.push(x)
        print(f"  push({x:>3}) → top={ms.top():>3}, min={ms.get_min():>3}")
    ms.pop()
    print(f"  pop()      → top={ms.top():>3}, min={ms.get_min():>3}")
