"""
05 · 前缀和 / 差分 —— 和为 K 的子数组

═══════════════════════════════════════════════════════════════
【题目】 LeetCode 560. 和为 K 的子数组
═══════════════════════════════════════════════════════════════

给一个整数数组 nums 和一个整数 k，求数组中「和恰好等于 k」的
连续子数组的个数（可以有负数，不能排序）。

例子：

    nums = [1, 1, 1], k = 2
    答案 = 2      （[1,1] 出现两次：下标 0..1 与 1..2）

    nums = [1, 2, 3], k = 3
    答案 = 2      （[1,2] 与 [3]）

═══════════════════════════════════════════════════════════════
【什么时候想到这个套路】
═══════════════════════════════════════════════════════════════

  - 题目问「区间和 / 子数组和」→ 先想前缀和。
  - 有负数，不能用滑动窗口（窗口和不单调，收缩逻辑失效）。
  - 「和为 K 的子数组个数」= 查找「差为某个目标」→ 前缀和 + 哈希。
  - 若是「频繁对区间批量加值」→ 想差分数组（本文附例 LC1109）。

═══════════════════════════════════════════════════════════════
【思路】前缀和 + 哈希，把 O(n²) 压到 O(n)
═══════════════════════════════════════════════════════════════

定义前缀和 prefix[i] = nums[0] + ... + nums[i-1]（prefix[0] = 0）。
那么子数组 nums[j..i-1] 的和 = prefix[i] - prefix[j]。

我们要它等于 k：

    prefix[i] - prefix[j] = k
    =>  prefix[j] = prefix[i] - k

所以：**固定右端 i，看历史上有多少个前缀和等于 prefix[i] - k**，
每一个这样的 j 都对应一个和为 k 的子数组。

用一个哈希表 count 记录「每种前缀和出现过几次」，边扫边查边加：

    nums:      1     1     1        k = 2
    prefix: 0     1     2     3
            ↑
      cur=0 时先放进哈希：{0:1}
      cur=1  找 1-2=-1 → 没有；哈希 {0:1, 1:1}
      cur=2  找 2-2= 0 → 有 1 个！ans=1；哈希 {0:1,1:1,2:1}
      cur=3  找 3-2= 1 → 有 1 个！ans=2；哈希 {...}

关键点：哈希里要**先塞入 prefix[0]=0**（对应「从头开始就凑齐 k」的情况），
且要「先查再更新」，避免把当前这个前缀和自己算进去（那是长度为 0 的空区间）。

═══════════════════════════════════════════════════════════════
【附例】 LeetCode 1109. 航班预订统计 —— 差分数组
═══════════════════════════════════════════════════════════════

n 个航班（编号 1..n），一批预订记录 (first, last, seats) 表示
「第 first 到 last 班，每班各加 seats 个座位」。求每班最终座位数。

朴素做法：每条记录都对区间逐个 += seats，O(区间长度)，最坏 O(n·m)。

差分数组：只在区间两端「打标记」，最后一次前缀和还原，O(n + m)。

    diff[first]     += seats     区间起点：从这里开始多 seats
    diff[last + 1]  -= seats     区间终点后一位：从这里开始撤销

    最后对 diff 求前缀和，就是每个位置的累计值。

    n=5, bookings=[[1,2,10],[2,3,20],[2,5,25]]
    diff(1..n+1，用 1 基便于理解):
      [1,2,10] :  diff[1]+=10, diff[3]-=10
      [2,3,20] :  diff[2]+=20, diff[4]-=20
      [2,5,25] :  diff[2]+=25, diff[6]-=25
    diff = [_,10,45,-10,-20,0,-25]   (下标 0 占位)
    前缀和 = [10, 55, 45, 25, 25]    ← 答案

═══════════════════════════════════════════════════════════════
【复杂度】
═══════════════════════════════════════════════════════════════

  LC560 : 时间 O(n)      空间 O(n)   （哈希表最多存 n 种前缀和）
  LC1109: 时间 O(n + m)  空间 O(n)   （m 为预订条数）
"""
from __future__ import annotations

from collections import defaultdict


# ────────────────────────────────────────────────────────────
# 主例：LC560 和为 K 的子数组（前缀和 + 哈希）
# ────────────────────────────────────────────────────────────

def subarray_sum(nums: list[int], k: int) -> int:
    """返回和恰好为 k 的连续子数组个数。"""
    # count[s] = 到目前为止「前缀和 == s」出现了几次
    # 先塞入 0：代表「空前缀」，让「从下标 0 开始的子数组」也能被匹配到
    count: dict[int, int] = defaultdict(int)
    count[0] = 1

    cur = 0          # 当前前缀和 prefix[i]
    ans = 0
    for x in nums:
        cur += x
        # 要找和为 k 的子数组，等价于找历史前缀和 == cur - k 的个数
        # 先查再更新：避免把当前前缀和自己当成配对项（那是空区间）
        ans += count[cur - k]
        # 记录当前前缀和，供后面的右端点查询
        count[cur] += 1
    return ans


# ────────────────────────────────────────────────────────────
# 附例：LC1109 航班预订统计（差分数组）
# ────────────────────────────────────────────────────────────

def corp_flight_bookings(bookings: list[list[int]], n: int) -> list[int]:
    """bookings[i] = [first, last, seats]（航班 1..n），返回每班座位数。"""
    # diff 多留一位，方便在 last+1 处做撤销标记而不越界
    diff = [0] * (n + 1)
    for first, last, seats in bookings:
        # 区间起点 +seats：从 first 开始「多出」seats
        diff[first - 1] += seats
        # 区间终点之后 -seats：到 last 为止，last+1 起撤销
        if last < n:
            diff[last] -= seats

    # 对差分数组求前缀和，还原出每个位置的真实累计值
    ans = [0] * n
    running = 0
    for i in range(n):
        running += diff[i]
        ans[i] = running
    return ans


# ────────────────────────────────────────────────────────────
# 自测 + 演示
# ────────────────────────────────────────────────────────────

if __name__ == "__main__":
    # ---- LC560 正确性 ----
    assert subarray_sum([1, 1, 1], 2) == 2
    assert subarray_sum([1, 2, 3], 3) == 2
    assert subarray_sum([1], 0) == 0
    assert subarray_sum([-1, -1, 1], 0) == 1        # 含负数，滑窗失效，前缀和照样对
    assert subarray_sum([3, 4, 7, 2, -3, 1, 4, 2], 7) == 4

    # ---- LC1109 正确性 ----
    assert corp_flight_bookings([[1, 2, 10], [2, 3, 20], [2, 5, 25]], 5) == [10, 55, 45, 25, 25]
    assert corp_flight_bookings([[1, 2, 10], [2, 2, 15]], 2) == [10, 25]

    print("=" * 63)
    print("LC560 · 和为 K 的子数组（前缀和 + 哈希）")
    print("=" * 63)
    nums, k = [3, 4, 7, 2, -3, 1, 4, 2], 7
    count: dict[int, int] = defaultdict(int)
    count[0] = 1
    cur = ans = 0
    print(f"nums = {nums}, k = {k}")
    print(f"{'读入':>6} | {'前缀和':>6} | {'查 cur-k':>8} | {'命中':>4} | 累计答案")
    print("-" * 50)
    for x in nums:
        cur += x
        hit = count[cur - k]
        ans += hit
        count[cur] += 1
        print(f"{x:>6} | {cur:>6} | {cur - k:>8} | {hit:>4} | {ans}")
    print(f"\n和为 {k} 的子数组共有 {ans} 个\n")

    print("=" * 63)
    print("LC1109 · 航班预订统计（差分数组做区间批量加）")
    print("=" * 63)
    bookings, n = [[1, 2, 10], [2, 3, 20], [2, 5, 25]], 5
    print(f"n = {n}, bookings = {bookings}")
    result = corp_flight_bookings(bookings, n)
    for i, seats in enumerate(result, start=1):
        print(f"  航班 {i}: {seats} 个座位")
