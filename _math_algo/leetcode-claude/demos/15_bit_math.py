"""
15 · 位运算 / 数学 —— 只出现一次的数字 & 快速幂 & 位 1 的个数

═══════════════════════════════════════════════════════════════
【题目】 LeetCode 136 只出现一次的数字 / LeetCode 50 Pow(x, n) / LeetCode 191 位1的个数
═══════════════════════════════════════════════════════════════

LC136：数组里每个元素都出现两次，只有一个出现一次，找出它。
       要求 O(n) 时间、O(1) 额外空间。

    nums = [4, 1, 2, 1, 2]  ->  4

LC50：实现 pow(x, n)，即计算 x 的 n 次幂（n 可能为负）。

    x = 2.0, n = 10  ->  1024.0

LC191：返回整数二进制表示中 1 的个数（汉明重量）。

    n = 11 (0b1011)  ->  3

═══════════════════════════════════════════════════════════════
【什么时候想到这个套路】
═══════════════════════════════════════════════════════════════

信号：题面出现「配对/成对出现」「集合的子集枚举」「幂/次方」
「GCD/最大公约数」「统计二进制位」，或要求 O(1) 空间、O(log n) 时间。
这类题往往有一个漂亮的位运算或数学恒等式，避开暴力。

常用位运算恒等式：
  a ^ a = 0，a ^ 0 = a，异或满足交换律结合律 -> 成对抵消。
  n & (n - 1) 会把 n 最低位的那个 1 清成 0 -> 数 1 的个数。
  x^n 可用「平方 + 折半指数」快速幂，O(log n) 而非 O(n)。

═══════════════════════════════════════════════════════════════
【思路】
═══════════════════════════════════════════════════════════════

  LC136：把所有数异或到一起。成对的数两两异或抵消为 0，最后剩下的
        就是那个唯一出现一次的数。O(n) 时间、O(1) 空间，无需哈希表。

  LC50：快速幂。x^n = (x^(n/2))^2，指数每次折半 -> O(log n)。
        n 为负时先取 x = 1/x、n = -n。用循环写法避免递归栈开销。

  LC191：反复执行 n = n & (n - 1)，每次抹掉最低位的 1，
        执行了几次就有几个 1。比逐位移位更快（只循环 1 的个数次）。

═══════════════════════════════════════════════════════════════
【复杂度】
═══════════════════════════════════════════════════════════════

  LC136：时间 O(n)，空间 O(1)。
  LC50：时间 O(log n)，空间 O(1)。
  LC191：时间 O(popcount)（1 的个数），空间 O(1)。
"""
from __future__ import annotations


# ────────────────────────────────────────────────────────────
# LC136 只出现一次的数字：异或抵消
# ────────────────────────────────────────────────────────────

def single_number(nums: list[int]) -> int:
    """找出唯一出现一次的元素。"""
    result = 0
    for x in nums:
        # 异或的魔法：a^a=0，a^0=a。成对的数彼此抵消，
        # 顺序无所谓（异或可交换），扫完只剩那个落单的数。
        result ^= x
    return result


# ────────────────────────────────────────────────────────────
# LC50 Pow(x, n)：快速幂
# ────────────────────────────────────────────────────────────

def my_pow(x: float, n: int) -> float:
    """计算 x 的 n 次幂。"""
    # 负指数：x^(-n) = (1/x)^n，先归一化成非负指数处理。
    if n < 0:
        x = 1 / x
        n = -n

    result = 1.0
    # 把指数 n 看成二进制。x 不断自平方（x, x², x⁴, x⁸ ...），
    # 对应 n 的某一位是 1 时，就把当前的 x 乘进结果。
    # 例：n=10=1010b -> 用到 x² 和 x⁸，result = x² * x⁸ = x^10。
    while n > 0:
        if n & 1:            # 当前最低位是 1
            result *= x
        x *= x               # x 升级为下一档平方
        n >>= 1              # 指数右移一位，看下一位
    return result


# ────────────────────────────────────────────────────────────
# LC191 位 1 的个数：n & (n-1) 消最低位 1
# ────────────────────────────────────────────────────────────

def hamming_weight(n: int) -> int:
    """返回二进制中 1 的个数。"""
    count = 0
    while n:
        # n & (n-1) 恰好把最低位的那个 1 变成 0，其余不变。
        # 循环几次就说明有几个 1 —— 只跑「1 的个数」次，而非 32 次。
        n &= n - 1
        count += 1
    return count


# ────────────────────────────────────────────────────────────
# 附加：欧几里得算法求最大公约数（数学经典）
# ────────────────────────────────────────────────────────────

def gcd(a: int, b: int) -> int:
    """辗转相除：gcd(a, b) = gcd(b, a mod b)，直到余数为 0。"""
    while b:
        a, b = b, a % b
    return a


# ────────────────────────────────────────────────────────────
# 主程序：断言正确性 + 打印可读轨迹
# ────────────────────────────────────────────────────────────

if __name__ == "__main__":
    # ---- LC136 ----
    assert single_number([4, 1, 2, 1, 2]) == 4
    assert single_number([2, 2, 1]) == 1
    assert single_number([1]) == 1

    # ---- LC50 ----
    assert my_pow(2.0, 10) == 1024.0
    assert my_pow(2.0, 0) == 1.0
    assert abs(my_pow(2.0, -2) - 0.25) < 1e-9
    assert abs(my_pow(3.0, 5) - 243.0) < 1e-9

    # ---- LC191 ----
    assert hamming_weight(11) == 3      # 0b1011
    assert hamming_weight(128) == 1     # 0b10000000
    assert hamming_weight(0) == 0
    assert hamming_weight(0b1111) == 4

    # ---- GCD ----
    assert gcd(12, 18) == 6
    assert gcd(17, 5) == 1
    assert gcd(100, 0) == 100

    print("=" * 60)
    print("15 · 位运算 / 数学")
    print("=" * 60)

    print("\n【LC136 只出现一次】把所有数异或到一起，成对抵消")
    nums = [4, 1, 2, 1, 2]
    acc = 0
    for x in nums:
        acc ^= x
        print(f"  ^ {x} -> 累积异或 = {acc}")
    print(f"  落单的数 = {acc}")

    print("\n【LC50 快速幂】x=2, n=10：按指数二进制逐位平方相乘")
    print(f"  2 ^ 10 = {my_pow(2.0, 10)}（只做 log2(10)≈4 次乘法档位）")

    print("\n【LC191 位1的个数】n & (n-1) 每次消掉一个最低位的 1")
    n = 11
    print(f"  {n} 的二进制 = {bin(n)} -> 1 的个数 = {hamming_weight(n)}")

    print("\n【附加 GCD】辗转相除 gcd(12, 18)")
    print(f"  gcd(12, 18) = {gcd(12, 18)}")

    print("\n全部断言通过 ✅")
